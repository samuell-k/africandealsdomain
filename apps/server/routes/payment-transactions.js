router.post('/', requireAuth, async (req, res) => {
  try {
    const {
      order_id,
      payment_method_id,
      payment_type = 'automatic',
      amount,
      currency = 'USD'
    } = req.body;

    // Validate required fields
    if (!order_id || !amount) {
      return res.status(400).json({
        success: false,
        error: 'Order ID and amount are required'
      });
    }

    // Verify order exists and belongs to user
    const [orders] = await pool.execute(`
      SELECT id, user_id, total_amount, status 
      FROM orders 
      WHERE id = ? AND user_id = ?
    `, [order_id, req.user.id]);

    if (orders.length === 0) {
      return res.status(404).json({
        success: false,
        error: 'Order not found or access denied'
      });
    }

    const order = orders[0];

    // Verify amount matches order total
    if (parseFloat(amount) !== parseFloat(order.total_amount)) {
      return res.status(400).json({
        success: false,
        error: 'Payment amount does not match order total'
      });
    }

    // Create payment transaction
    const [result] = await pool.execute(`
      INSERT INTO payment_transactions (
        order_id, payment_method_id, payment_type, amount, currency, status
      ) VALUES (?, ?, ?, ?, ?, 'pending')
    `, [order_id, payment_method_id || null, payment_type, amount, currency]);

    const transactionId = result.insertId;

    // Log the action
    await logPaymentAction(transactionId, 'TRANSACTION_CREATED', {
      order_id,
      payment_method_id,
      payment_type,
      amount,
      currency
    }, req.user.id, req);

    res.status(201).json({
      success: true,
      transaction: {
        id: transactionId,
        order_id,
        payment_method_id,
        payment_type,
        amount,
        currency,
        status: 'pending'
      }
    });
  } catch (error) {
    console.error('Error creating payment transaction:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to create payment transaction'
    });
  }
});

